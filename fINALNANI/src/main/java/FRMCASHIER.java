    private void txtCodeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCodeFocusLost
      String code = txtCode.getText().trim();
    String filePath = "C:\\Users\\WS\\Documents\\NetBeansProjects\\BERKABZ\\your_file_name.csv"; // Update with your file name and extension

    try (BufferedReader br = new BufferedReader(new FileReader(filePath))) {
        String line;
        boolean found = false;

        while ((line = br.readLine()) != null) {
            String[] parts = line.split(","); // Assuming CSV format; adjust split based on your file format

            // Check if the code matches the data in the file
            if (parts.length >= 4 && parts[0].trim().equals(code)) {
                lblName.setText(parts[1].trim()); // Assuming name is in the second column
                lblPrice.setText(parts[2].trim()); // Assuming price is in the third column
                // Populate other fields similarly using appropriate column indices

                found = true;
                break;
            }
        }

        if (!found) {
            txtCode.setBackground(Color.pink);
            JOptionPane.showMessageDialog(this, "NO PRODUCT FOUND!", "INVALID", JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (IOException e) {
        e.printStackTrace(); // Handle file reading errors appropriately
    }

    }//GEN-LAST:event_txtCodeFocusLost

    private void txtQuantityFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtQuantityFocusLost
        // TODO add your handling code here:
        String x = txtQuantity.getText().trim();
        if(x.isEmpty()){
            txtQuantity.setBackground(Color.red);
            JOptionPane.showMessageDialog(this, "You need to input a quantity!","Invalid Quanitity",JOptionPane.ERROR_MESSAGE);
            txtQuantity.setBackground(Color.white);
            txtQuantity.setText("");
        }else {
            int quantity = Integer.parseInt(x);
            if(quantity > quantity){
                txtQuantity.setBackground(Color.red);
                JOptionPane.showMessageDialog(this, "Remaining Stock " + quantity,"Invalid Quanitity",JOptionPane.ERROR_MESSAGE);
                txtQuantity.setBackground(Color.white);
                txtQuantity.setText(Integer.toString(quantity));
                // txtQuantity.requestFocus();
            }else{
                float total = quantity * (float)quantity;
                lblTotal.setText(Float.toString(total));
            }
        }

    }//GEN-LAST:event_txtQuantityFocusLost

