    private void txtCashFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCashFocusLost
        // TODO add your handling code here:
        txtCash.setBackground(Color.yellow);
        txtCode.setForeground(Color.orange);
        String total = lblTotal.getText();
        String amount = txtCash.getText();
        if (amount.length()> 0){
            float total_flt = Float.parseFloat(total);
            float amount_flt =Float.parseFloat(amount);
            if(amount_flt < total_flt){
                JOptionPane.showMessageDialog(this, "Not enough money!");
            }else{
                float change_flt = amount_flt - total_flt;
                String change = Float.toString(change_flt);
                lblChange.setText(change);
                if (!txtCash.getText().equals("")) {
                    lblCheckout.setEnabled(true);
                }
            }
        }
    }//GEN-LAST:event_txtCashFocusLost

