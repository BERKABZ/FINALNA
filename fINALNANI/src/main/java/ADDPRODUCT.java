    private void productpriceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_productpriceActionPerformed
      String enteredPrice = productprice.getText();
    try {
        double priceValue = Double.parseDouble(enteredPrice);
        
      
        if (priceValue <= 0) {
            JOptionPane.showMessageDialog(this, "Please enter a valid price.", "Invalid Price", JOptionPane.WARNING_MESSAGE);
            return;
        }

      
        JOptionPane.showMessageDialog(this, "Entered Product Price: " + priceValue, "Product Price", JOptionPane.INFORMATION_MESSAGE);
    } catch (NumberFormatException e) {
     
        JOptionPane.showMessageDialog(this, "Please enter a valid numeric price.", "Invalid Price", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_productpriceActionPerformed

