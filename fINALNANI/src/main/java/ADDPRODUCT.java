    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
  if (!isLoggedIn()) {
            JOptionPane.showMessageDialog(this, "Please log in first.", "Login Required", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String code = productcode.getText();
        String name = productname.getText();
        String price = productprice.getText();
        String stock = productstock.getText();

        if (code.isEmpty() || name.isEmpty() || price.isEmpty() || stock.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all the fields.", "Incomplete Fields", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String productInfo = "product code: " + code + "\n"
                            + "product name: " + name + "\n"
                            + "product price: " + price + "\n"
                            + "product stock: " + stock + "\n\n";

        String filePath = "C:\\Users\\WS\\Documents\\NetBeansProjects\\BERKABZ\\your_file_name.csv";

        try (BufferedWriter writer = new BufferedWriter(new FileWriter(filePath, true))) {
            writer.write(productInfo);
            System.out.println("Product added to file successfully!");

            JOptionPane.showMessageDialog(this, "Product added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException e) {
            System.out.println("Error writing to file: " + e.getMessage());
            JOptionPane.showMessageDialog(this, "Error writing to file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private boolean isLoggedIn() {
       
        return true; 
    

    }//GEN-LAST:event_SubmitActionPerformed

